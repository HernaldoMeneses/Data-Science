--#~§¬    SQL as Part of the Data Analysis Workflow
--#~§¬    Analysis work always starts with a question
--#~§¬    Question to take a action
--#~§¬    Once the question is framed we consider:
--#~§¬          where the data originated           (Souce systems) 
--#~§¬               Data can be generated by
--#~§¬                   people by hand
--#~§¬                      ex. during a doctor’s visit
--#~§¬                   machine generated
--#~§¬                      ex. application database
--#~§¬                      ex. event-streaming system records a website click
--#~§¬                      ex. marketing management tool records an email open
--#~§¬          where the data is stored            (Data warehouse)
--#~§¬              Souce an Target
--#~§¬              ETL - Extract, Transform and Load
--#~§¬                  Souce - Where the data comes from
--#~§¬                  Target - Destination
--#~§¬          the analysis plan                   (Queries and analysis) -- foco do livro
--#~§¬              SQL is applied to explore, profile, clean, shape, and analyze the data
--#~§¬                  Explore
--#~§¬                      - where data was generated
--#~§¬                      - knowing Tables
--#~§¬                  Profiling
--#~§¬                      - checking the unique values and distribution of records in the data set.
--#~§¬                  Cleaning
--#~§¬                      - involves fixing incorrect or incomplete data, adding categorization and flags, and handling null values.
--#~§¬                  Shaping
--#~§¬                      - process of arranging the data into the rows and columns needed in the result set.
--#~§¬                  Analyzing
--#~§¬                      - involves reviewing the output for trends, conclusions, and insights.
--#~§¬          and how result will be presented    (Presentation - Visualization, report, dashboard, paper)
